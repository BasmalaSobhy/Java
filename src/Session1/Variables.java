package Session1;

public class Variables {
    public static void main(String[] args) {


    /* Variables
        used to store values

    >> dataType variableName = value; >> variable initialization >> we create a variable and give it a starting value at the same time

    >> dataType variableName; >> variable Declaration >> we create the variable only;

    >> variableName = value; >> variable assignment >> we give a value to an existing variable


    Note: java is case sensitive so 'j' is not equal 'J'
     */

        int x = 5; // variable initialization

        int y; // variable Declaration
        y = 5; // variable assignment

    /* Data Types
        The type of data stored in a variable

    Note: Variables that are declared without an initial value are set to the default value.

        Types with their default value
        1. int >> 0
        2. float >> 0.0f
        3. double >> 0.0d
        4. short >> 0
        5. long >> 0L
        6. byte >> 0
        7. char >> '\u0000'
        8. boolean >> false
     */

        int i = 5;
        float f = 5.2f;
        double d = 5.2d;
        double D = 5.2;
        short s = 5;
        long l = 5;
        long L = 5L;
        byte b = 5;
        char c = 'c';
        boolean bool = true;
        boolean Bool = false;

    }
}